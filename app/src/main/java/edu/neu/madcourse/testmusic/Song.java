package edu.neu.madcourse.testmusic;

public class Song {
    private String name;
    private String artist;
    private int bpm;
    private String url;

    public Song(){};

    public String getName() {
        return name;
    }

    public String getArtist() {
        return artist;
    }

    public int getBpm() {
        return bpm;
    }

    public String getUrl() {
        return url;
    }
}
